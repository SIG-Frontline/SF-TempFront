	<h3 class="text-xl font-bold my-2">Advanced Schedule Builder (In-Progress)</h3>
	<blockquote>
		<p class="text-base text-wrap">
			The Advanced Schedule Builder extends the features of the current <a
				href="https://myhub.njit.edu/scbldr/">Schedule Builder</a
			>, with a variety of new improvements:
		</p>
		<ul class=" ml-8 my-2 list-disc">
			<li>Refreshed UI</li>
			<li>Course Filtering (Online, Honors, Time, Day, Subject, Building, etc.)</li>
			<li>Custom Events</li>
			<li>Prerequisite Validation</li>
			<li>Calendar Export (& Other Types)</li>
			<li>Plans</li>
			<li>
				and most importantly, <strong style="display:inline">D A R K M O D E</strong>
			</li>
		</ul>
		<p class="text-base">
			We are hoping to have a MVP finished in Summer '24, and potentially propose it to IST to
			replace the current Schedule Builder.
		</p>
	</blockquote>
	<h3 class="text-xl font-bold my-3">Room Availability (Not Started)</h3>
	<blockquote>
		<p class="text-base">
			Show all rooms in NJIT and whether they are in use or not, pulling class data as well as
			events from HighlanderHub.
		</p>
		<p class="text-base">
